# What is the difference between lists, tuples and sets?
 
# list: 
# A list is collection of data which ordered and changeable( it is mutable)

k=['a', 'c', 'd', 'e']
print(k)
print(type(k)) 

# tuple
# A tuple is a collection of data which ordered and unchangeable( it is immutable)

y=('v', 'e', 1)
print(y)
print(type(y))

# set
# Set is a collection of data which is unordered, unchangeable and unindexed and have unique data.
m={1,2,2,3,4,5,6}
print(m)

# dict
# An unordered mutable collection of key-valuee pairs.
e={'m':'akd', "adf":234}
print(e)
